                        variant="outline"
                        onClick={handleRefreshPrices}
                        disabled={isLoading}
                        className="flex items-center gap-1 px-2 py-1 text-xs h-7"
                    >
                        <RefreshCw className={`w-3 h-3 ${isLoading ? 'animate-spin' : ''}`} />
